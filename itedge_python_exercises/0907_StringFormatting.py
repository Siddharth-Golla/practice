"""Assume you have a cat and dog.
The cat's age is 5 and the dog's age is 3.
A dog year is equal to 7 human years.
A cat year is equal to 5 human years.
Print out both pet's age in human years.
"""

cat_age = 5
dog_age = 3
dog_human_years = dog_age * 7
cat_human_years = cat_age * 5
print(f"The cat is {cat_human_years} in human years.")
print(f"The dog is {dog_human_years} in human years.")